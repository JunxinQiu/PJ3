let stationCounter = 0;

const getStationId = () => { stationCounter++; return ['S', stationCounter].join()};

const generateBusStop = (x,y) => {
    return {
        id: getStationId(),
        x,
        y
    }
}

const generateBusLine = (name, directionList) => {
    return {
        name,
        stations: directionList.map(([x, y]) => generateBusStop(x, y))
    }
}

